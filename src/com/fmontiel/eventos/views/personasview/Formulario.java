    public static enum Modos {
        AGREGAR,
        EDITAR
    }

    public Formulario(
            Formulario.Modos modo,
            Long eventoId,
            PersonaModel persona,
            CallbackInterface onFinish) {

        this.modo = modo;
        this.eventoId = eventoId;
        this.persona = persona;
        this.onFinish = onFinish;
        this.personasService = new PersonasService();

        initComponents();

        if (modo == Formulario.Modos.EDITAR) {
            textFieldCUI.setText(persona.getId().toString());
            textFieldCUI.disable();
            textFieldNombre.setText(persona.getNombre());
            textFieldEmail.setText(persona.getEmail());
            textFieldTelefono.setText(persona.getTelefono());
            btnAccion.setText("Editar");
        }
    }

    private void agregarPersona() {
        try {
            PersonaModel nuevaPersona = new PersonaModel();
            nuevaPersona.setId(Long.valueOf(textFieldCUI.getText()));
            nuevaPersona.setNombre(textFieldNombre.getText());
            nuevaPersona.setEmail(textFieldEmail.getText());
            nuevaPersona.setTelefono(textFieldTelefono.getText());

            PersonaModel personaAgregada = personasService.addPersonaToEvento(eventoId, nuevaPersona.getId(), nuevaPersona.getNombre(), nuevaPersona.getEmail(), nuevaPersona.getTelefono());

            JOptionPane.showMessageDialog(this, "Persona agregada correctamente");
            onFinish.call(new JSONObject().put("status", "added").put("personaId", personaAgregada.getId()));
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error al agregar persona: " + e.getMessage());
        }
    }

    private void editarPersona() {
        try {
            
            persona.setNombre(textFieldNombre.getText());
            persona.setEmail(textFieldEmail.getText());
            persona.setTelefono(textFieldTelefono.getText());

            PersonaModel personaActualizada = personasService.updatePersonaInEvento(eventoId, persona.getId(), persona);

            JOptionPane.showMessageDialog(this, "Persona actualizada correctamente");
            onFinish.call(new JSONObject().put("status", "updated").put("personaId", personaActualizada.getId()));
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error al actualizar persona: " + e.getMessage());
        }
    }

    private void btnRealizaraccion(java.awt.event.ActionEvent evt) {                                         
        if (Formulario.Modos.AGREGAR == modo) {
            agregarPersona();
        } else if (Formulario.Modos.EDITAR == modo) {
            editarPersona();
        }
        dispose();
    }   
    
